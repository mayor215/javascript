// push- adds item(s) to the end of an array
let array = ['a' , 'b' , 'c' , 'd' , 'e' , 'f'];
array.push('g' , 1 , 'z');
console.log(array)

// pop- removes an item(s) from the end of an array
array = ['a' , 'b' , 'c' , 'd' , 'e' , 'f'];
array.pop();
array.pop();
console.log(array)

// shift removes an item(s) from the start of an array 
array = ['a' , 'b' , 'c' , 'd' , 'e' , 'f'];
array.shift();
console.log(array);

// unshift adds items to the start of an array
array = ['a' , 'b' , 'c' , 'd' , 'e' , 'f'];
array.unshift();
console.log(array);